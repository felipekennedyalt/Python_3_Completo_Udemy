#  Tipo de dado bool (booblean)
# ao questionar algo em um programa
# só existem duas respostas possíveis:
# sim (True) ou não (False) letras maiúsculas.
# existem vários operadores para "questionar".
# dentre eles o ==, que é um operador lógico que 
# questiona se um valor é igual a outro
#  exemplo:
print(10 == 10) #sim=> True (verdadeiro)
print(10 == 100) #não=> False (Falseo)
print(type(10 == 100)) # tipo bool
print(type(True)) 
print(type(False)) 
# A função type() vai retornar bool, pois é uma comparação com uma resposta de verdadiro ou falso, então é do tipo booleano
