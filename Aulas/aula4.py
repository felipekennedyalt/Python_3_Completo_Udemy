# Tipos int e float
# int -> Número inteiro
# O tipo int representa qualquer numero
# inteiro, positivo ou negativo, sem parte fracionaria. int sem sinal é considerado positivo
# print(11) #int
# print(-11) #int
# print(0) #int
# se quiser colocar tudo dentro de comentários, seleciona as linhas e aperta (Ctrl + ;)

# float -> Número real
# O tipo float representa qualquer numero real, positivo ou negativo, com parte fracionaria.
# float sem sinal é considerado positivo
# Exemplos de int e float
# print(1.1, 10.11) # float
# print(-1.0) # float
# print(0.0) # float

# A função type mostra o tipo que o python inferiu ao valor
print(type(1.1)) # float
print(type(1)) # int
print(type("111")) # str
