# conversão de tipos, coerção
# type convertion, typecasting, coercion
# é o ato de converter um tipo em outro
# tipos imutaveis e primitivos:
# str, int, float, bool

print(1 + 1) # =2
print("a" + "b") # = ab (Concatenação)
# print("a" + 2) # = erro tipos diferentes
print("a" + str(2)) # = a2 (Conversão de tipos)
print(bool('')) # = converte pra booleano, vazia é False
print(bool(' ')) # = converte pra booleano, e com qualquer outro valor é True



